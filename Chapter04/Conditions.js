// Operator Overview.
a = 2;
b = '2';

if (a == b) {
  console.log('== they are equal');
} else {
  console.log('== they are not equal');
}

if (a === b) {
  console.log('=== they are equal');
} else {
  console.log(' === they are not equal');
}

if (a <= b) {
  console.log('<= they are equal');
} else {
  console.log('<= they are not equal');
}

if (a >= b) {
  console.log('>= they are equal');
} else {
  console.log('>= they are not equal');
}
